<h1>Scrum...</h1>
<p>
Mas que una metodologia, es un framework en el que se aplican buenas practicas basado en un proceso de desarrollo incremental/iterativo para desarrollo agil.
Puede ser aplicado en mantenimientos, implementaciones y otros procesos, no solamente en un proceso de desarrollo.
</p><br>
<h2>Un poco de historia...</h2>
<p>
En 1986 Hirotaka Takeuchi e Ikujiro Nonaka describieron una nueva aproximacion holistica que incrementa la rapidez y la flexibilidad en el desarrollo de nuevos productos comerciales.
</p><br>
<h2>Un poco mas...</h2>
Algunas herramientas para la gestion de todo el proceso se listan:
<ul>
<li class="Lista">KUNAGI. Herramienta Web que proporciona un sistema de gestion de proyectos basado en Scrum. Ofrece herramientas colaborativas y otras facilidades, como un cuadro de mando del proyecto, un panel interactivo para el Sprint o soporte a la estimacion con Planning Poker.</li>
<li class="Lista">SCRUMDO. Herramienta Scrum muy centrada en la simplicidad y en la facilidad de uso. Permite gestionar las listas de tareas e historias de usuario, crear y gestionar iteraciones, obtener graficos de avance ''burndown'' y tambien dar soporte a la estimacion con Planning Poker.</li>
<li class="Lista">SPRINTOMETER. Herramienta para la gestion, medicion y seguimiento de proyectos Scrum y eXtreme Programming. Para simplificar el intercambio de datos permite exportar graficos e informes a Excel. Posee graficos de avance burndown en 3D.</li>
<li class="Lista">ICESCRUM. Herramienta Scrum y Kanban. Ofrece las opciones de operacion, consulta y estimacion de historias de usuario. Permite añadir historias de usuario a la pila de producto, dividir el tiempo en Sprints y mover estas historias de la pila de producto a cada uno de los Sprint. Posee la tecnica de Planning Poker para la estimacion y paneles virtuales.</li>
<li class="Lista">PANGO SCRUM. Otra de las herramientas Scrum online, con una interfaz sencilla y amigable que permite escribir, estimar y priorizar la pila de producto. Facilita en gran medida la planificacion de Sprints y las reuniones.</li>
</ul>
